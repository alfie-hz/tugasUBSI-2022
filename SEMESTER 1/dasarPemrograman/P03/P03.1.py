# Operator Penugasan / Assignment Operator

a = 10; # <- Nilai Awal
print("Nilai a :", a);

a += 1; # Artinya 10 + 1
print("Nilai a :", a);

print("");

b = 10; # <- Nilai Awal
print("Nilai b :", b);

b -= 1; # Artinya 10 - 1
print("Nilai b :", b);

print("");

c = 10;
print("Nilai c :", c);

c *= 5; # <- Artinya 10 x 5
print("Nilai c :", c);

print("");

d = 10;
print("Nilai d :", d);

d /= 2; # <- Artinya 10 : 2
print("Nilai d :", int(d));

print("");

e = 10;
print("Nilai e :", e);

e %= 3; # <- Sisa dari 10 % 3 = 1
print("Nilai e :", e);

print("");

f = 10;
print("Nilai f :", f);

f //= 3;
print("Nilai f :", f);

print("");